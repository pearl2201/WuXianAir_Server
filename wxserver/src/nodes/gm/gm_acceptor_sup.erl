-export([
	 init/1
        ]).

%% --------------------------------------------------------------------
%% Macros
%% --------------------------------------------------------------------

%% --------------------------------------------------------------------
%% Records
%% --------------------------------------------------------------------

%% ====================================================================
%% External functions
%% ====================================================================

start_link(Callback) ->
	supervisor:start_link({local,?MODULE},?MODULE, Callback).

%% ====================================================================
%% Server functions
%% ====================================================================
%% --------------------------------------------------------------------
%% Func: init/1
%% Returns: {ok,  {SupFlags,  [ChildSpec]}} |
%%          ignore                          |
%%          {error, Reason}
%% --------------------------------------------------------------------

init(Callback) ->
	{ok, {{simple_one_for_one, 10, 10},
	      [{gm_acceptor, {gm_acceptor, start_link, [Callback]},
		transient, brutal_kill, worker, [gm_acceptor]}]}}.

