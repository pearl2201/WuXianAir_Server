%% ====================================================================
%% External functions
%% ====================================================================

start_link() ->
	supervisor:start_link({local,?MODULE},?MODULE, []).

%% ====================================================================
%% Server functions
%% ====================================================================
%% --------------------------------------------------------------------
%% Func: init/1
%% Returns: {ok,  {SupFlags,  [ChildSpec]}} |
%%          ignore                          |
%%          {error, Reason}
%% --------------------------------------------------------------------
init([]) ->
    AChild = {loop_instance_mgr,{loop_instance_mgr,start_link,[]},
	      permanent,2000,worker,[loop_instance_mgr]},
    {ok,{{one_for_one,10,10}, [AChild]}}.